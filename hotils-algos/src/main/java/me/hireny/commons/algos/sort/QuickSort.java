package me.hireny.commons.algos.sort;

/**
 * QuickSort
 * 快速排序
 * @Author: hireny
 * @Date: Create in 2019/11/04 01:03
 */
public class QuickSort<T extends Comparable<? super T>> implements Sort<T> {
    @Override
    public T[] sort(T[] array) {
        return null;
    }

    @Override
    public char[] sort(char[] array) {
        return new char[0];
    }

    @Override
    public byte[] sort(byte[] array) {
        return new byte[0];
    }

    @Override
    public short[] sort(short[] array) {
        return new short[0];
    }

    @Override
    public int[] sort(int[] array) {
        return new int[0];
    }

    @Override
    public long[] sort(long[] array) {
        return new long[0];
    }

    @Override
    public float[] sort(float[] array) {
        return new float[0];
    }

    @Override
    public double[] sort(double[] array) {
        return new double[0];
    }
}
