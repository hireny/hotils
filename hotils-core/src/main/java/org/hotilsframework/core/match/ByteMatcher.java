package org.hotilsframework.core.match;

/**
 * ByteMatcher
 *
 * 字节匹配
 *
 * @author hireny
 * @create 2020-12-29 21:39
 */
public class ByteMatcher extends AbstractMatcher<Byte> implements Matcher<Byte> {

    public ByteMatcher(Byte value) {
        super(value);
    }

    @Override
    public boolean match(Byte aByte) {
        return super.match(aByte);
    }
}
